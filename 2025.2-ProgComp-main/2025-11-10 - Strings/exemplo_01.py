'''
    Exemplo 01: Trabalhando com fatiamento de strings
'''

strTexto = 'Rio Grande do Norte'

print(strTexto)      # Rio Grande do Norte

print(strTexto[8])   # d

print(strTexto[-3])  # r

print(strTexto[1:5])  # io G

print(strTexto[:10])  # Rio Grande

print(strTexto[10:]) # do Norte

print(strTexto[-5:])  # Norte